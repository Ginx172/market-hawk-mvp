    """Full training run report."""
    model_name: str
    num_classes: int
    class_names: List[str]
    total_epochs: int
    best_epoch: int
    best_val_accuracy: float
    best_val_f1: float
    epoch_metrics: List[TrainingMetrics] = field(default_factory=list)
    train_samples: int = 0
    val_samples: int = 0
    test_samples: int = 0
    total_elapsed_sec: float = 0.0
    confusion_matrix: Optional[List[List[int]]] = None

    def summary(self) -> str:
        """Format human-readable training summary."""
        lines = [
            "=" * 60,
            "CHART VISION MODEL TRAINING REPORT",
            "=" * 60,
            f"Model:              {self.model_name}",
            f"Classes:            {self.num_classes} ({', '.join(self.class_names[:5])}...)"
            if len(self.class_names) > 5 else
            f"Classes:            {self.num_classes} ({', '.join(self.class_names)})",
            f"Train samples:      {self.train_samples:,}",
            f"Val samples:        {self.val_samples:,}",
            f"Test samples:       {self.test_samples:,}",
            f"Total epochs:       {self.total_epochs}",
            f"Best epoch:         {self.best_epoch}",
            f"Best val accuracy:  {self.best_val_accuracy:.4f}",
            f"Best val F1:        {self.best_val_f1:.4f}",
            f"Total time:         {self.total_elapsed_sec:.1f}s",
            "=" * 60,
        ]
        return "\n".join(lines)


