class ChartPatternDataset(Dataset):
    """Lazy-loading chart image dataset from manifest CSV.

    Reads the manifest CSV generated by prepare_chart_dataset.py.
    Images are loaded on-the-fly (lazy loading) to conserve RAM.
    Supports training augmentations and validation transforms.

    Args:
        manifest_path: Path to the manifest CSV file.
        label_column: Column name to use as classification target.
        transform: torchvision transform pipeline.
        label_to_idx: Optional pre-built label-to-index mapping.
            If None, builds from manifest data.
        filter_unlabeled: If True, skip rows where label == "unlabeled".
    """

    def __init__(
        self,
        manifest_path: str,
        label_column: str = "label",
        transform: Any = None,
        label_to_idx: Optional[Dict[str, int]] = None,
        filter_unlabeled: bool = True,
    ) -> None:
        self.manifest_path = Path(manifest_path)
        self.label_column = label_column
        self.transform = transform
        self.filter_unlabeled = filter_unlabeled

        # Load manifest
        df = pd.read_csv(self.manifest_path)
        if filter_unlabeled:
            df = df[df[label_column] != "unlabeled"].reset_index(drop=True)

        self.image_paths: List[str] = df["image_path"].tolist()
        self.labels_raw: List[str] = df[label_column].tolist()

        # Build or use provided label mapping
        if label_to_idx is not None:
            self.label_to_idx = label_to_idx
        else:
            unique_labels = sorted(set(self.labels_raw))
            self.label_to_idx = {lbl: i for i, lbl in enumerate(unique_labels)}

        self.idx_to_label: Dict[int, str] = {
            v: k for k, v in self.label_to_idx.items()
        }
        self.num_classes: int = len(self.label_to_idx)

        logger.info(
            "Dataset loaded: %d samples, %d classes from %s",
            len(self), self.num_classes, self.manifest_path.name,
        )

    def __len__(self) -> int:
        return len(self.image_paths)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, int]:
        """Load a single image and return (tensor, label_index).

        Args:
            idx: Sample index.

        Returns:
            Tuple of (image_tensor, label_index).
        """
        from PIL import Image

        img_path = self.image_paths[idx]
        label_str = self.labels_raw[idx]
        label_idx = self.label_to_idx.get(label_str, 0)

        # Load image, convert to RGB
        try:
            img = Image.open(img_path).convert("RGB")
        except Exception as e:
            logger.warning("Failed to load image %s: %s", img_path, e)
            # Return a black placeholder image
            img = Image.new("RGB", (INPUT_SIZE, INPUT_SIZE), (0, 0, 0))

        if self.transform is not None:
            img = self.transform(img)
        else:
            # Fallback: basic tensor conversion
            from torchvision import transforms
            img = transforms.Compose([
                transforms.Resize((INPUT_SIZE, INPUT_SIZE)),
                transforms.ToTensor(),
                transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
            ])(img)

        return img, label_idx

    @property
    def class_names(self) -> List[str]:
        """Return ordered list of class names."""
        return [self.idx_to_label[i] for i in range(self.num_classes)]


def get_train_transforms() -> Any:
    """Build training augmentation pipeline.

    Returns:
        torchvision.transforms.Compose for training data.
    """
    from torchvision import transforms
    return transforms.Compose([
        transforms.Resize((INPUT_SIZE, INPUT_SIZE)),
        transforms.RandomHorizontalFlip(p=0.5),
        transforms.ColorJitter(
            brightness=0.2, contrast=0.2, saturation=0.1, hue=0.05,
        ),
        transforms.RandomRotation(degrees=5),
        transforms.ToTensor(),
        transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
    ])


def get_val_transforms() -> Any:
    """Build validation/test transform pipeline (no augmentation).

    Returns:
        torchvision.transforms.Compose for validation/test data.
    """
    from torchvision import transforms
    return transforms.Compose([
        transforms.Resize((INPUT_SIZE, INPUT_SIZE)),
        transforms.ToTensor(),
        transforms.Normalize(mean=IMAGENET_MEAN, std=IMAGENET_STD),
    ])


