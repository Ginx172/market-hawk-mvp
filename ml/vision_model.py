class ChartPatternClassifier(nn.Module):
    """ResNet50-based chart pattern classifier with transfer learning.

    Architecture:
        - Backbone: ResNet50 pretrained on ImageNet
        - Freeze: First 3 residual blocks (layer1, layer2, layer3)
        - Fine-tune: layer4 + custom classification head
        - Head: AdaptiveAvgPool2d -> Dropout(0.3) -> Linear(2048, 512)
                -> ReLU -> Dropout(0.2) -> Linear(512, num_classes)

    Args:
        num_classes: Number of output classes.
        pretrained: Whether to use ImageNet pretrained weights.
        dropout_head: Dropout rate for the first dropout layer.
        dropout_classifier: Dropout rate for the second dropout layer.
    """

    def __init__(
        self,
        num_classes: int,
        pretrained: bool = True,
        dropout_head: float = 0.3,
        dropout_classifier: float = 0.2,
    ) -> None:
        super().__init__()
        self.num_classes = num_classes

        # Load pretrained ResNet50
        from torchvision import models
        if pretrained:
            weights = models.ResNet50_Weights.IMAGENET1K_V2
            self.backbone = models.resnet50(weights=weights)
        else:
            self.backbone = models.resnet50(weights=None)

        # Freeze early layers (conv1, bn1, layer1, layer2, layer3)
        frozen_modules = [
            self.backbone.conv1,
            self.backbone.bn1,
            self.backbone.layer1,
            self.backbone.layer2,
            self.backbone.layer3,
        ]
        for module in frozen_modules:
            for param in module.parameters():
                param.requires_grad = False

        # Replace the default FC head
        backbone_out_features = self.backbone.fc.in_features  # 2048
        self.backbone.fc = nn.Identity()

        # Custom classification head
        self.classifier = nn.Sequential(
            nn.AdaptiveAvgPool1d(1) if False else nn.Identity(),
            nn.Dropout(p=dropout_head),
            nn.Linear(backbone_out_features, 512),
            nn.ReLU(inplace=True),
            nn.Dropout(p=dropout_classifier),
            nn.Linear(512, num_classes),
        )

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """Forward pass.

        Args:
            x: Input tensor of shape (batch, 3, 224, 224).

        Returns:
            Logits tensor of shape (batch, num_classes).
        """
        features = self.backbone(x)  # (batch, 2048) after avgpool + flatten
        logits = self.classifier(features)
        return logits

    def get_trainable_params(self) -> int:
        """Count trainable parameters."""
        return sum(p.numel() for p in self.parameters() if p.requires_grad)

    def get_total_params(self) -> int:
        """Count total parameters."""
        return sum(p.numel() for p in self.parameters())


