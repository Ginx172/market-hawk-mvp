def predict_chart(
    image_path: str,
    model_path: str = "models/vision/chart_vision_best.pt",
    device: Optional[str] = None,
) -> Dict[str, Any]:
    """Predict chart pattern from a single image.

    Args:
        image_path: Path to the chart image.
        model_path: Path to saved model checkpoint.
        device: Device string ("cuda", "cpu"). Auto-detected if None.

    Returns:
        Dict with keys:
            - pattern: str -- predicted pattern name
            - confidence: float -- probability of predicted pattern
            - probabilities: Dict[str, float] -- all class probabilities
    """
    from PIL import Image

    # Device
    if device is None:
        dev = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    else:
        dev = torch.device(device)

    # Load checkpoint
    ckpt = torch.load(model_path, map_location=dev, weights_only=False)
    num_classes = ckpt["num_classes"]

    # Rebuild model
    model = ChartPatternClassifier(num_classes=num_classes, pretrained=False)
    model.load_state_dict(ckpt["model_state_dict"])
    model.to(dev)
    model.eval()

    # Load and transform image
    transform = get_val_transforms()
    img = Image.open(image_path).convert("RGB")
    img_tensor = transform(img).unsqueeze(0).to(dev)

    # Inference
    with torch.no_grad():
        with torch.cuda.amp.autocast(enabled=(dev.type == "cuda")):
            logits = model(img_tensor)
            probs = torch.softmax(logits, dim=1).squeeze(0)

    # Need class names from training -- try to load from report
    report_path = Path(model_path).parent / "training_report.json"
    if report_path.exists():
        with open(report_path, "r", encoding="utf-8") as f:
            report_data = json.load(f)
        class_names = report_data.get("class_names", [])
    else:
        class_names = [f"class_{i}" for i in range(num_classes)]

    probs_np = probs.cpu().numpy()
    pred_idx = int(probs_np.argmax())
    confidence = float(probs_np[pred_idx])

    probabilities = {
        class_names[i]: float(probs_np[i])
        for i in range(len(class_names))
    }

    # Cleanup
    del model, img_tensor, logits, probs
    torch.cuda.empty_cache()
    gc.collect()

    return {
        "pattern": class_names[pred_idx] if pred_idx < len(class_names) else f"class_{pred_idx}",
        "confidence": confidence,
        "probabilities": probabilities,
    }
