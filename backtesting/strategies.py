class AgentConsensusStrategy(StrategyBase):
    """
    Market Hawk 5-Agent Consensus Strategy.

    Runs the full Brain pipeline (ML Signal, Knowledge Advisor,
    Risk Manager, News Analyzer, Security Guard) and uses
    the consensus score for trade decisions.

    This is the strategy that makes Market Hawk unique —
    it backtests the actual multi-agent system.
    """

    def __init__(self, consensus_threshold: float = 0.30,
                 sl_pct: float = 0.02, tp_pct: float = 0.04):
        self.consensus_threshold = consensus_threshold
        self.sl_pct = sl_pct
        self.tp_pct = tp_pct
        self.brain = None
        self._feature_engineer = None

    @property
    def name(self) -> str:
        return f"AgentConsensus_thresh{self.consensus_threshold}"

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        """Pre-compute all 60 features the ML engine needs."""
        from data.market_data_fetcher import MarketDataFetcher
        self._feature_engineer = MarketDataFetcher()

        # Engineer features on the full historical dataset
        df_features = self._feature_engineer.engineer_features(df.copy())

        # Merge features back (they'll have the same index)
        for col in df_features.columns:
            if col not in df.columns:
                df[col] = df_features[col].values

        return df

    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        """
        For backtesting, we use a simplified consensus based on
        the pre-computed features rather than calling async agents.
        This is much faster and reproducible.
        """
        if idx < 200:  # Need enough history for indicators
            return TradeSignal(Signal.HOLD)

        try:
            # Continuous-score ensemble vote from indicators
            # Each voter outputs a score in [-1.0, +1.0] for granular consensus
            votes = []

            # Vote 1: RSI — continuous scoring
            # Maps RSI 0→100 to score +1.0→-1.0 (low RSI = bullish)
            rsi = df.get("RSI", df.get("rsi", pd.Series([50])))
            if isinstance(rsi, pd.Series) and idx < len(rsi):
                rsi_val = rsi.iloc[idx]
                if not pd.isna(rsi_val):
                    # Linear map: RSI 30→+0.8, RSI 50→0, RSI 70→-0.8
                    rsi_score = np.clip((50 - rsi_val) / 25.0, -1.0, 1.0)
                    votes.append(("RSI", rsi_score))

            # Vote 2: MACD histogram — direction + magnitude
            macd_hist = df.get("MACD_histogram", df.get("macd", pd.Series([0])))
            if isinstance(macd_hist, pd.Series) and idx < len(macd_hist):
                mh = macd_hist.iloc[idx]
                if not pd.isna(mh):
                    # Normalize by recent range for comparable scale
                    lookback = max(0, idx - 50)
                    recent_hist = macd_hist.iloc[lookback:idx+1].dropna()
                    if len(recent_hist) > 5:
                        hist_range = recent_hist.abs().quantile(0.95)
                        if hist_range > 0:
                            macd_score = np.clip(mh / hist_range, -1.0, 1.0)
                        else:
                            macd_score = 1.0 if mh > 0 else -1.0
                    else:
                        macd_score = 1.0 if mh > 0 else -1.0
                    votes.append(("MACD", macd_score))

            # Vote 3: MA trend — spread strength
            sma20 = df.get("SMA_20", pd.Series([0]))
            sma50 = df.get("SMA_50", pd.Series([0]))
            if isinstance(sma20, pd.Series) and idx < len(sma20):
                s20 = sma20.iloc[idx]
                s50 = sma50.iloc[idx] if isinstance(sma50, pd.Series) and idx < len(sma50) else s20
                if not pd.isna(s20) and not pd.isna(s50) and s50 != 0:
                    # Score proportional to spread (capped at ±2%)
                    spread_pct = (s20 - s50) / s50
                    ma_score = np.clip(spread_pct / 0.02, -1.0, 1.0)
                    votes.append(("MA_Trend", ma_score))

            # Vote 4: Bollinger position — continuous
            bb_pos = df.get("BB_position", pd.Series([0.5]))
            if isinstance(bb_pos, pd.Series) and idx < len(bb_pos):
                bp = bb_pos.iloc[idx]
                if not pd.isna(bp):
                    # Map: 0.0→+1.0 (oversold), 0.5→0, 1.0→-1.0 (overbought)
                    bb_score = np.clip(1.0 - 2.0 * bp, -1.0, 1.0)
                    votes.append(("BB", bb_score))

            # Vote 5: Volume confirmation (amplifier, not directional)
            vol_ratio = df.get("volume_ratio", pd.Series([1.0]))
            if isinstance(vol_ratio, pd.Series) and idx < len(vol_ratio):
                vr = vol_ratio.iloc[idx]
                if not pd.isna(vr):
                    # High volume amplifies existing signal direction
                    vol_amplifier = np.clip((vr - 1.0) / 2.0, 0.0, 1.0)
                    # Use direction of other votes as volume's direction
                    if len(votes) > 0:
                        direction = sum(v[1] for v in votes) / len(votes)
                        vol_score = direction * vol_amplifier
                    else:
                        vol_score = 0.0
                    votes.append(("Volume", vol_score))

            if not votes:
                return TradeSignal(Signal.HOLD)

            # Calculate consensus
            total_score = sum(v[1] for v in votes) / len(votes)

            if total_score > self.consensus_threshold:
                return TradeSignal(
                    signal=Signal.BUY,
                    confidence=min(abs(total_score), 0.95),
                    stop_loss_pct=self.sl_pct,
                    take_profit_pct=self.tp_pct,
                    reason=f"Agent consensus={total_score:.3f} ({len(votes)} votes)",
                    metadata={"votes": {v[0]: v[1] for v in votes}},
                )
            elif total_score < -self.consensus_threshold:
                return TradeSignal(
                    signal=Signal.SELL,
                    confidence=min(abs(total_score), 0.95),
                    stop_loss_pct=self.sl_pct,
                    take_profit_pct=self.tp_pct,
                    reason=f"Agent consensus={total_score:.3f} ({len(votes)} votes)",
                    metadata={"votes": {v[0]: v[1] for v in votes}},
                )

            return TradeSignal(Signal.HOLD)

        except Exception:
            logger.debug("Signal generation error at idx %d", idx, exc_info=True)
            return TradeSignal(Signal.HOLD)
