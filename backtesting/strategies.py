# ============================================================
# SIGNAL TYPES
# ============================================================

class Signal(Enum):
    BUY = "BUY"
    SELL = "SELL"
    HOLD = "HOLD"


@dataclass
class TradeSignal:
    """Signal generated by a strategy."""
    signal: Signal
    confidence: float = 0.5       # 0.0 to 1.0
    stop_loss_pct: float = 0.02   # 2% default
    take_profit_pct: float = 0.04 # 4% default
    position_size: float = 0.02   # 2% of portfolio
    reason: str = ""
    metadata: Dict[str, Any] = None

    def __post_init__(self):
        if self.metadata is None:
            self.metadata = {}


# ============================================================
# BASE STRATEGY
# ============================================================

class StrategyBase(ABC):
    """
    Abstract base class for all backtesting strategies.

    Subclass this and implement:
        - name: str property
        - generate_signal(df, idx) -> TradeSignal
        - optionally: on_init(df) for pre-computation
    """

    @property
    @abstractmethod
    def name(self) -> str:
        """Strategy name for reporting."""
        ...

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        """
        Called once before backtesting starts.
        Pre-compute indicators here and add them as columns.
        Return the modified DataFrame.
        """
        return df

    @abstractmethod
    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        """
        Generate a trading signal for the current bar.

        Args:
            df: Full DataFrame with indicators
            idx: Current bar index (integer position)

        Returns:
            TradeSignal (BUY, SELL, or HOLD)
        """
        ...


