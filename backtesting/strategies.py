# MA CROSSOVER STRATEGY
# ============================================================

class MACrossover(StrategyBase):
    """
    Moving Average Crossover (Golden/Death Cross).

    BUY:  Fast MA crosses above Slow MA
    SELL: Fast MA crosses below Slow MA
    """

    def __init__(self, fast_period: int = 20, slow_period: int = 50,
                 use_ema: bool = True,
                 sl_pct: float = 0.02, tp_pct: float = 0.04):
        self.fast_period = fast_period
        self.slow_period = slow_period
        self.use_ema = use_ema
        self.sl_pct = sl_pct
        self.tp_pct = tp_pct

    @property
    def name(self) -> str:
        ma_type = "EMA" if self.use_ema else "SMA"
        return f"{ma_type}_{self.fast_period}/{self.slow_period}_Cross"

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        close = df["Close"]
        if self.use_ema:
            df["_fast_ma"] = close.ewm(span=self.fast_period, adjust=False).mean()
            df["_slow_ma"] = close.ewm(span=self.slow_period, adjust=False).mean()
        else:
            df["_fast_ma"] = close.rolling(self.fast_period).mean()
            df["_slow_ma"] = close.rolling(self.slow_period).mean()
        return df

    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        if idx < 1:
            return TradeSignal(Signal.HOLD)

        fast_now = df["_fast_ma"].iloc[idx]
        slow_now = df["_slow_ma"].iloc[idx]
        fast_prev = df["_fast_ma"].iloc[idx - 1]
        slow_prev = df["_slow_ma"].iloc[idx - 1]

        if pd.isna(fast_now) or pd.isna(slow_now):
            return TradeSignal(Signal.HOLD)

        # Golden cross
        if fast_prev <= slow_prev and fast_now > slow_now:
            spread = abs(fast_now - slow_now) / slow_now
            conf = min(0.5 + spread * 20, 0.95)
            return TradeSignal(
                signal=Signal.BUY,
                confidence=conf,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"Golden Cross: fast={fast_now:.4f} > slow={slow_now:.4f}",
            )

        # Death cross
        if fast_prev >= slow_prev and fast_now < slow_now:
            spread = abs(slow_now - fast_now) / slow_now
            conf = min(0.5 + spread * 20, 0.95)
            return TradeSignal(
                signal=Signal.SELL,
                confidence=conf,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"Death Cross: fast={fast_now:.4f} < slow={slow_now:.4f}",
            )

        return TradeSignal(Signal.HOLD)


