# RSI MEAN REVERSION
# ============================================================

class RSIMeanReversion(StrategyBase):
    """
    RSI Mean Reversion Strategy.

    BUY:  RSI drops below oversold level then starts recovering
    SELL: RSI rises above overbought level then starts declining
    """

    def __init__(self, period: int = 14,
                 oversold: float = 30.0, overbought: float = 70.0,
                 sl_pct: float = 0.015, tp_pct: float = 0.03):
        self.period = period
        self.oversold = oversold
        self.overbought = overbought
        self.sl_pct = sl_pct
        self.tp_pct = tp_pct

    @property
    def name(self) -> str:
        return f"RSI_{self.period}_MeanRev({self.oversold}/{self.overbought})"

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        delta = df["Close"].diff()
        gain = delta.where(delta > 0, 0.0)
        loss = -delta.where(delta < 0, 0.0)
        avg_gain = gain.rolling(self.period).mean()
        avg_loss = loss.rolling(self.period).mean()
        rs = avg_gain / avg_loss
        df["_rsi"] = 100 - (100 / (1 + rs))
        return df

    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        if idx < 2:
            return TradeSignal(Signal.HOLD)

        rsi_now = df["_rsi"].iloc[idx]
        rsi_prev = df["_rsi"].iloc[idx - 1]
        rsi_prev2 = df["_rsi"].iloc[idx - 2]

        if pd.isna(rsi_now) or pd.isna(rsi_prev):
            return TradeSignal(Signal.HOLD)

        # Oversold bounce: RSI was below oversold, now recovering
        if rsi_prev2 < self.oversold and rsi_prev < self.oversold and rsi_now > self.oversold:
            depth = (self.oversold - min(rsi_prev, rsi_prev2)) / self.oversold
            conf = min(0.55 + depth, 0.90)
            return TradeSignal(
                signal=Signal.BUY,
                confidence=conf,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"RSI oversold bounce: {rsi_prev:.1f} -> {rsi_now:.1f}",
            )

        # Overbought reversal: RSI was above overbought, now declining
        if rsi_prev2 > self.overbought and rsi_prev > self.overbought and rsi_now < self.overbought:
            depth = (max(rsi_prev, rsi_prev2) - self.overbought) / (100 - self.overbought)
            conf = min(0.55 + depth, 0.90)
            return TradeSignal(
                signal=Signal.SELL,
                confidence=conf,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"RSI overbought reversal: {rsi_prev:.1f} -> {rsi_now:.1f}",
            )

        return TradeSignal(Signal.HOLD)


