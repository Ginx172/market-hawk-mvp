# BOLLINGER BANDS BREAKOUT
# ============================================================

class BollingerBreakout(StrategyBase):
    """
    Bollinger Bands Breakout/Reversion.

    BUY:  Price touches lower band and starts bouncing
    SELL: Price touches upper band and starts declining
    """

    def __init__(self, period: int = 20, num_std: float = 2.0,
                 sl_pct: float = 0.02, tp_pct: float = 0.03):
        self.period = period
        self.num_std = num_std
        self.sl_pct = sl_pct
        self.tp_pct = tp_pct

    @property
    def name(self) -> str:
        return f"BB_{self.period}_{self.num_std}std"

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        close = df["Close"]
        df["_bb_mid"] = close.rolling(self.period).mean()
        bb_std = close.rolling(self.period).std()
        df["_bb_upper"] = df["_bb_mid"] + self.num_std * bb_std
        df["_bb_lower"] = df["_bb_mid"] - self.num_std * bb_std
        df["_bb_pct"] = (close - df["_bb_lower"]) / (df["_bb_upper"] - df["_bb_lower"])
        return df

    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        if idx < 1:
            return TradeSignal(Signal.HOLD)

        pct_now = df["_bb_pct"].iloc[idx]
        pct_prev = df["_bb_pct"].iloc[idx - 1]
        close = df["Close"].iloc[idx]

        if pd.isna(pct_now) or pd.isna(pct_prev):
            return TradeSignal(Signal.HOLD)

        # Bounce from lower band
        if pct_prev < 0.05 and pct_now > 0.05:
            return TradeSignal(
                signal=Signal.BUY,
                confidence=0.65,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"BB lower band bounce: {pct_prev:.2f} -> {pct_now:.2f}",
            )

        # Rejection from upper band
        if pct_prev > 0.95 and pct_now < 0.95:
            return TradeSignal(
                signal=Signal.SELL,
                confidence=0.65,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"BB upper band rejection: {pct_prev:.2f} -> {pct_now:.2f}",
            )

        return TradeSignal(Signal.HOLD)


