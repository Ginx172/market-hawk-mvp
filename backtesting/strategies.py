# MACD HISTOGRAM STRATEGY
# ============================================================

class MACDHistogram(StrategyBase):
    """
    MACD Histogram reversal strategy.

    BUY:  Histogram crosses from negative to positive
    SELL: Histogram crosses from positive to negative
    """

    def __init__(self, fast: int = 12, slow: int = 26, signal: int = 9,
                 sl_pct: float = 0.02, tp_pct: float = 0.04):
        self.fast = fast
        self.slow = slow
        self.signal_period = signal
        self.sl_pct = sl_pct
        self.tp_pct = tp_pct

    @property
    def name(self) -> str:
        return f"MACD_{self.fast}_{self.slow}_{self.signal_period}"

    def on_init(self, df: pd.DataFrame) -> pd.DataFrame:
        close = df["Close"]
        ema_fast = close.ewm(span=self.fast, adjust=False).mean()
        ema_slow = close.ewm(span=self.slow, adjust=False).mean()
        df["_macd"] = ema_fast - ema_slow
        df["_macd_signal"] = df["_macd"].ewm(span=self.signal_period, adjust=False).mean()
        df["_macd_hist"] = df["_macd"] - df["_macd_signal"]
        return df

    def generate_signal(self, df: pd.DataFrame, idx: int) -> TradeSignal:
        if idx < 1:
            return TradeSignal(Signal.HOLD)

        hist_now = df["_macd_hist"].iloc[idx]
        hist_prev = df["_macd_hist"].iloc[idx - 1]

        if pd.isna(hist_now) or pd.isna(hist_prev):
            return TradeSignal(Signal.HOLD)

        # Bullish crossover
        if hist_prev < 0 and hist_now > 0:
            return TradeSignal(
                signal=Signal.BUY,
                confidence=0.60,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"MACD bullish: hist {hist_prev:.4f} -> {hist_now:.4f}",
            )

        # Bearish crossover
        if hist_prev > 0 and hist_now < 0:
            return TradeSignal(
                signal=Signal.SELL,
                confidence=0.60,
                stop_loss_pct=self.sl_pct,
                take_profit_pct=self.tp_pct,
                reason=f"MACD bearish: hist {hist_prev:.4f} -> {hist_now:.4f}",
            )

        return TradeSignal(Signal.HOLD)


